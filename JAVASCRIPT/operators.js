//ARITHMETIC OPERATORS
console.log(10 + 3); //13 addition
console.log(10 - 3); //7 subtraction
console.log(10 * 3); //30 multiplication
console.log(10 / 3); //3.333... division
console.log(10 % 3); //1 - remainder(modulo) - very useful
console.log(10 ** 2); //100 exponentiation

// COMPARISON OPERATORS - always return true or false
console.log(5 === 5); //true - strict equality (USE THIS)
console.log(5 === '5'); //false - strict equality
console.log(5 == '5'); //true - loose equality - NEVER USE THI
console.log(5 !== 3); //true - strict inequality
console.log(10 > 5); //true - greater than
console.log(10 < 5); //false - less than
console.log(10 >= 10); //true - greater than or equal to

// LOGICAL OPERATORS - combine conditions
console.log(true && true); //true --AND: both must be true
console.log(true && false); //false --AND: one is false so false
console.log(true || false); //true --OR: at least one must be true
console.log(!true); //false --NOT: flips it